    public function getData()
    {
        $data = Company::all();
        return Datatables::of($data)
            ->addIndexColumn()
            ->addColumn('action', function ($data) {
                return '<a href="' . route('company.edit', $data->id) . '" class="modal-show edit" title="Company: ' . $data->name . ' ">
                    <i class="fa fa-pencil"></i>
                </a>';
            })
            ->rawColumns(['action'])
            ->make(true);
    }
