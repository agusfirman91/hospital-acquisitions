    public function getData()
    {
        $data = Category::all();
        return Datatables::of($data)
            ->addIndexColumn()
            ->addColumn('action', function ($data) {
                return '<a href="' . route('stock.edit', $data->id) . '" class="modal-show edit" title="Stock: ' . $data->id . ' ">
                    <i class="fa fa-pencil"></i>
                </a>';
            })
            ->rawColumns(['action'])
            ->make(true);
    }


